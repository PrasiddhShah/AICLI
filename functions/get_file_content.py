import os
from google.genai import types
MAX_CHARS = 10000
def get_file_content(working_directory,file_path):
    try :
        absPath = os.path.abspath(working_directory)
        targetDir = os.path.normpath(os.path.join(absPath,file_path))
        if os.path.commonpath([absPath,targetDir]) != absPath:
            return f'Error: Cannot list "{file_path}" as it is outside the permitted working directory'
        if not os.path.isfile(targetDir):
            return f'Error: File not found or is not a regular file: "{file_path}"'
        fileContentString =""
        with open(targetDir,"r") as f:
            fileContentString = f.read(MAX_CHARS)
            if f.read(1):
                fileContentString += f' [...File "{file_path}" truncated at {MAX_CHARS} characters]'
        return fileContentString
    except Exception as e:
        return f"Error: {e}"
    
def file_content_schema():
    schema_get_file_content = types.FunctionDeclaration(
        name="get_file_content",
        description=f"Reads and returns the text content of a file. Content is truncated at {MAX_CHARS} characters if the file is larger.",
        parameters=types.Schema(
            type=types.Type.OBJECT,
            properties={
                "file_path": types.Schema(
                    type=types.Type.STRING,
                    description="Path to the file to read, relative to the working directory",
                ),
            },
            required=["file_path"],
        ),
    )
    return schema_get_file_content